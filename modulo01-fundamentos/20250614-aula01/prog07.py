"""
Operadores em Python

Operadores de comparação.

Operadores de comparação, como o próprio nome já diz, são operadores que irão comparar valores. Essa comparação sempre irá retornar um valor booleano (True ou False)
"""

if __name__ == "__main__":

    # Maior que
    print(50 > 40)      # True

    # Maior ou igual a
    print(13 >= 13)     # True

    # Menor que
    print(4 < 2)        # False

    # Menor ou igual a
    print(34 <= 27)     # False

    # Igual a
    print(10 == 10)     # True

    # Diferente de
    print(45 != 45)     # False